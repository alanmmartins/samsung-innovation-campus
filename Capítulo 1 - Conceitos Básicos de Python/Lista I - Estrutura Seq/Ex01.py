#escreva um programa em python que receba quatro numeros inteiros, calcule e mostre a soma desses

# Solicita ao usuário que insira quatro números inteiros
num1 = int(input("Digite o primeiro número inteiro: "))
num2 = int(input("Digite o segundo número inteiro: "))
num3 = int(input("Digite o terceiro número inteiro: "))
num4 = int(input("Digite o quarto número inteiro: "))

# Calcula a soma dos números
soma = num1 + num2 + num3 + num4

# Exibe o resultado
print("A soma dos quatro números é:", soma)
