#Usando os operadores lógicos "and", "or" e "not" escreva um programa que, dado x = 1 e y = 0, imprima os seguintes resultados: 


# 0
# 1
# False
# True 
# Definindo as variáveis
x = 1
y = 0

# Imprimindo os resultados conforme solicitado
print(x and y)     # 0
print(x or y)      # 1
print(not x)       # False
print(not y)       # True
